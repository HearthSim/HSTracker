    init?(rawString: String) {
        var string = rawString.lowercased()
        if string == "class" {
            string = "tag_class"
        }
        for _enum in GameTag.allValues() {
            if "\(_enum)" == string {
                self = _enum
                return
            }
        }
        if let value = Int(rawString), let _enum = GameTag(rawValue: value) {
            self = _enum
            return
        }
        return nil
    }

